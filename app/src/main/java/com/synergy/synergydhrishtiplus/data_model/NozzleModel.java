package com.synergy.synergydhrishtiplus.data_model;

public class NozzleModel {
    String pumpId;
    String nozzleId;
    String makeOfDispenser;
    String volume;
    String amount;
    String product;
    String status;
    String price;

    public String getPumpId() {
        return pumpId;
    }

    public void setPumpId(String pumpId) {
        this.pumpId = pumpId;
    }

    public String getNozzleId() {
        return nozzleId;
    }

    public void setNozzleId(String nozzleId) {
        this.nozzleId = nozzleId;
    }

    public String getMakeOfDispenser() {
        return makeOfDispenser;
    }

    public void setMakeOfDispenser(String makeOfDispenser) {
        this.makeOfDispenser = makeOfDispenser;
    }

    public String getVolume() {
        return volume;
    }

    public void setVolume(String volume) {
        this.volume = volume;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getProduct() {
        return product;
    }

    public void setProduct(String product) {
        this.product = product;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
